package week03.t03_triple;

public class Triple <T, K, V> {
	
	private T a;
    private T b;
    private T c;
    
	public T getA() {
		return a;
	}
	public void setA(T a) {
		this.a = a;
	}
	public T getB() {
		return b;
	}
	public void setB(T b) {
		this.b = b;
	}
	public T getC() {
		return c;
	}
	public void setC(T c) {
		this.c = c;
	}
       
}
